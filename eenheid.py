    def kan_ik_tot_hier(self, game):
        kan_ik_tot_hier = False
        self.bewegen = self.start_bewegen
        begin_x_verschil = game.aangepaste_x - self.x
        begin_y_verschil = game.aangepaste_y - self.y
        positief_x_verschil = abs(begin_x_verschil)
        positief_y_verschil = abs(begin_y_verschil)

        rooster_positie_x = self.x // 16
        rooster_positie_y = self.y // 16

        if self.is_geselecteerd:
            # print(f"eenheid op {rooster_positie_x}:{rooster_positie_y}, verschil met pointer {begin_x_verschil}:{begin_y_verschil}")
            xi = 0
            yi = 0
            while positief_x_verschil > 0 or positief_y_verschil > 0:
                if positief_x_verschil > 0:
                    positief_x_verschil -= 16
                    if begin_x_verschil < 0:
                        xi -= 1
                    else:
                        xi += 1
                if positief_y_verschil > 0:
                    positief_y_verschil -= 16
                    if begin_y_verschil < 0:
                        yi -= 1
                    else:
                        yi += 1
                if positief_x_verschil < 0 and positief_y_verschil < 0:
                    break
                pad_x = rooster_positie_x + xi
                pad_y = rooster_positie_y + yi
                terrein_op_pad = self.vlakterrein[pad_y][pad_x]
                terrein_impact_op_pad = game.ondergrond_impact[terrein_op_pad]
                self.bewegen -= game.ondergrond_impact[self.vlakterrein[int(self.y / 16 + yi)][int(self.x / 16) + xi]]
                # print(f"\tterrein {terrein_op_pad} met impact {terrein_impact_op_pad} op pad_positie {pad_x}:{pad_y}. Bewegingsruimte={self.bewegen}")
                # if self.bewegen >= 0:
                #     game.highlight(self.x + xi * 16, self.y + yi * 16)
            if self.bewegen >= 0:
                kan_ik_tot_hier = True
        return kan_ik_tot_hier






